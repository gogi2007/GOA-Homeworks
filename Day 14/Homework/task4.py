#4) შექმენით list'ი 5 ელემენტით და შეცვალეთ მისი ყველა მნიშვნელობა ცალცალკე, გამოიტანეთ საწყისი და საბოლოო შედეგები# Creating a list with 5 elements

my_list = ['apple', 'banana', 'cherry', 'date', 'elderberry']


print("Initial list:", my_list)


my_list[0] = 'avocado'    # Change 'apple' to 'avocado'
my_list[1] = 'blueberry'  # Change 'banana' to 'blueberry'
my_list[2] = 'coconut'    # Change 'cherry' to 'coconut'
my_list[3] = 'dragonfruit' # Change 'date' to 'dragonfruit'
my_list[4] = 'fig'        # Change 'elderberry' to 'fig'


print("Final list:", my_list)
