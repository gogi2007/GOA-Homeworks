#5) შექმენით list'ი 5 ელემენტით, და შეცვალეთ მისი მეორე და მესამე ელემენტის მნიშვნელობები slicing'ის მეშვეობით (მხოლოდ 1 ხაზში), გამოიტანეთ საწყისი და საბოლოო შედეგები


my_list = ['apple', 'banana', 'cherry', 'date', 'elderberry']


print("Initial list:", my_list)

# Changing the values of the second and third elements using slicing
my_list[1:3] = ['blueberry', 'coconut']


print("Final list:", my_list)
